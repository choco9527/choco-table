// 生成随机汉字
function getRandomChineseWord(num = 1) {
  const arr = []
  for (let i = 0; i < num; i++) {
    var _rsl = ''
    const _randomUniCode = Math.floor(Math.random() * (40870 - 19968) + 19968).toString(16)
    eval('_rsl=' + '"\\u' + _randomUniCode + '"')
    arr.push(_rsl)
  }
  return arr.join('')
}

